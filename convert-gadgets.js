// 解析CSV行
function parseCSVLine(line) {
    const result = [];
    let current = '';
    let inQuotes = false;

    for (let i = 0; i < line.length; i++) {
        const char = line[i];

        if (char === '"') {
            inQuotes = !inQuotes;
        } else if (char === ',' && !inQuotes) {
            result.push(current.trim());
            current = '';
        } else {
            current += char;
        }
    }
    result.push(current.trim());
    return result;
}

// 转换函数
function convertCSVToJS(startLine = 1, endLine = 100) {
    const gadgets = [];

    for (let i = startLine; i < Math.min(endLine + 1, lines.length); i++) {
        if (!lines[i] || lines[i].trim() === '') continue;

        const values = parseCSVLine(lines[i]);
        if (values.length < headers.length) continue;

        const japaneseName = values[0];
        const chineseName = chineseNames[japaneseName] || values[1]; // 如果没有中文名，使用英文名

        const gadget = {
            id: `gadget_${i - 1}`,
            japanese_name: japaneseName,
            english_name: values[1],
            chinese_name: chineseName,
            first_appearance_episode: values[2] === '-' ? null : values[2],
            function: values[3],
            used_by: values[4],
            main_user: values[5],
            category: values[6],
            success_rate: parseInt(values[7]) || 0,
            failure_rate: parseInt(values[8]) || 0,
            is_recurring: values[9] === 'Yes',
            notes: values[10] || ''
        };

        gadgets.push(gadget);
    }

    return gadgets;
}

